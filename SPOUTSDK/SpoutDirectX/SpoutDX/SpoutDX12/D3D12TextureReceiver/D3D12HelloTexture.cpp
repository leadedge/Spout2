// SPOUT
// Select sender (see Win32Application::WindowProc WM_RBUTTONDOWN)
// For a key, centre on the application window
// Default, centre on the cursor position
void D3D12HelloTexture::OnKeyUp(UINT8 key)
{
    if (key == 83) { // 's'
        // For a key, pass in the window handle to centre 
        // the selection dialog on the application window
        receiver.SelectSender(Win32Application::GetHwnd());
    }

    // Ctrl-S message is sent from 
    // Win32Application::WindowProc WM_RBUTTONDOWN
    if (key == 19) { // 'Ctrl-S'
        // No argument for mouse click to centre the
        // selection dialog on the cursor position
        receiver.SelectSender();
    }
}


// Render the scene.
void D3D12HelloTexture::OnRender()
{
    // Record all the commands we need to render the scene into the command list.
    PopulateCommandList();

    // Execute the command list.
    ID3D12CommandList* ppCommandLists[] = { m_commandList.Get() };
    m_commandQueue->ExecuteCommandLists(_countof(ppCommandLists), ppCommandLists);

    // Present the frame.
    ThrowIfFailed(m_swapChain->Present(1, 0));

    WaitForPreviousFrame();

	//
	// SPOUT
	//

	// Receive a sender's texture to a D3D12 texture resource
	if (receiver.ReceiveDX12Resource(&g_pReceivedResource12)) {

		//
		// Sender details can be retrieved with :
		//		const char * GetSenderName();
		//		unsigned int GetSenderWidth();
		//		unsigned int GetSenderHeight();
		//		DXGI_FORMAT GetSenderFormat();
		//		HANDLE GetSenderHandle;
		//		long GetSenderFrame();
		//		double GetSenderFps();
		//
		
		// Create or re-create the receiving texture for a new sender or if the sender size changed
		if (receiver.IsUpdated()) {

			// Release the receiving D3D12 texture resource
			if (g_pReceivedResource12)
				g_pReceivedResource12->Release();
			g_pReceivedResource12 = nullptr;

            // Create a D3D12 texture resource to use for the SRV
			DXGI_FORMAT texformat = receiver.GetSenderFormat();
			receiver.CreateDX12texture(m_device.Get(),
				receiver.GetSenderWidth(),
				receiver.GetSenderHeight(),
				D3D12_RESOURCE_STATE_COPY_DEST, // The texture will be copied to 
				texformat, // The sender texture format
				&g_pReceivedResource12);

			// Any other action required by the receiver can be done here
			// In this example, we re-create the shader resource view
			D3D12_SHADER_RESOURCE_VIEW_DESC srvDesc = {};
			srvDesc.Shader4ComponentMapping = D3D12_DEFAULT_SHADER_4_COMPONENT_MAPPING;
			srvDesc.Format = texformat; // SRV and texture format must match
			srvDesc.ViewDimension = D3D12_SRV_DIMENSION_TEXTURE2D;
			srvDesc.Texture2D.MipLevels = 1;
			m_device->CreateShaderResourceView(g_pReceivedResource12, &srvDesc, m_srvHeap->GetCPUDescriptorHandleForHeapStart());

		} // end receiver was updated

		// If a new texture has been received, the application can
		// perform any processing required here instead of every frame.
		// None is required in this example because we have already
		// created the Shader Resource View.

		// if (receiver.IsFrameNew()) {
		//
		// }

	}
	else {
		
		// A sender was not found or the connected sender closed.
		// The receiving texture resource is re-created when another sender is found.

		// Restore the initial shader resource view.
		// Note that it must be the original texture format - RGBA.
		D3D12_SHADER_RESOURCE_VIEW_DESC srvDesc = {};
		srvDesc.Shader4ComponentMapping = D3D12_DEFAULT_SHADER_4_COMPONENT_MAPPING;
		srvDesc.Format = DXGI_FORMAT_R8G8B8A8_UNORM;
		srvDesc.ViewDimension = D3D12_SRV_DIMENSION_TEXTURE2D;
		srvDesc.Texture2D.MipLevels = 1;
		m_device->CreateShaderResourceView(m_texture.Get(), &srvDesc, m_srvHeap->GetCPUDescriptorHandleForHeapStart());

	}

}

void D3D12HelloTexture::OnDestroy()
{
    // Ensure that the GPU is no longer referencing resources that are about to be
    // cleaned up by the destructor.
    WaitForPreviousFrame();

    CloseHandle(m_fenceEvent);

	// SPOUT
	receiver.CloseDirectX12();

}

void D3D12HelloTexture::PopulateCommandList()
{
    // Command list allocators can only be reset when the associated 
    // command lists have finished execution on the GPU; apps should use 
    // fences to determine GPU execution progress.
    ThrowIfFailed(m_commandAllocator->Reset());

    // However, when ExecuteCommandList() is called on a particular command 
    // list, that command list can then be reset at any time and must be before 
    // re-recording.
    ThrowIfFailed(m_commandList->Reset(m_commandAllocator.Get(), m_pipelineState.Get()));

    // Set necessary state.
    m_commandList->SetGraphicsRootSignature(m_rootSignature.Get());

    ID3D12DescriptorHeap* ppHeaps[] = { m_srvHeap.Get() };
    m_commandList->SetDescriptorHeaps(_countof(ppHeaps), ppHeaps);

    m_commandList->SetGraphicsRootDescriptorTable(0, m_srvHeap->GetGPUDescriptorHandleForHeapStart());
    m_commandList->RSSetViewports(1, &m_viewport);
    m_commandList->RSSetScissorRects(1, &m_scissorRect);

    // Indicate that the back buffer will be used as a render target.
    m_commandList->ResourceBarrier(1, &CD3DX12_RESOURCE_BARRIER::Transition(m_renderTargets[m_frameIndex].Get(), D3D12_RESOURCE_STATE_PRESENT, D3D12_RESOURCE_STATE_RENDER_TARGET));

    CD3DX12_CPU_DESCRIPTOR_HANDLE rtvHandle(m_rtvHeap->GetCPUDescriptorHandleForHeapStart(), m_frameIndex, m_rtvDescriptorSize);
    m_commandList->OMSetRenderTargets(1, &rtvHandle, FALSE, nullptr);

    // Record commands.
    const float clearColor[] = { 0.0f, 0.2f, 0.4f, 1.0f };
    m_commandList->ClearRenderTargetView(rtvHandle, clearColor, 0, nullptr);
    // SPOUT - full quad
    // m_commandList->IASetPrimitiveTopology(D3D_PRIMITIVE_TOPOLOGY_TRIANGLELIST);
    m_commandList->IASetPrimitiveTopology(D3D_PRIMITIVE_TOPOLOGY_TRIANGLESTRIP);
    m_commandList->IASetVertexBuffers(0, 1, &m_vertexBufferView);
    // SPOUT - full quad
    // m_commandList->DrawInstanced(3, 1, 0, 0);
    m_commandList->DrawInstanced(4, 1, 0, 0);

    // Indicate that the back buffer will now be used to present.
    m_commandList->ResourceBarrier(1, &CD3DX12_RESOURCE_BARRIER::Transition(m_renderTargets[m_frameIndex].Get(), D3D12_RESOURCE_STATE_RENDER_TARGET, D3D12_RESOURCE_STATE_PRESENT));

    ThrowIfFailed(m_commandList->Close());
}

void D3D12HelloTexture::WaitForPreviousFrame()
{
    // WAITING FOR THE FRAME TO COMPLETE BEFORE CONTINUING IS NOT BEST PRACTICE.
    // This is code implemented as such for simplicity. The D3D12HelloFrameBuffering
    // sample illustrates how to use fences for efficient resource usage and to
    // maximize GPU utilization.

    // Signal and increment the fence value.
    const UINT64 fence = m_fenceValue;
    ThrowIfFailed(m_commandQueue->Signal(m_fence.Get(), fence));
    m_fenceValue++;

    // Wait until the previous frame is finished.
    if (m_fence->GetCompletedValue() < fence)
    {
        ThrowIfFailed(m_fence->SetEventOnCompletion(fence, m_fenceEvent));
        WaitForSingleObject(m_fenceEvent, INFINITE);
    }

    m_frameIndex = m_swapChain->GetCurrentBackBufferIndex();
}
